nombre = "Martin"

# Concatenar

bienvenida = "Hola " + nombre + " todo bien?"

# Esta forma de concatenar o incluir variables se les denominan "F strings" por eso hay que poner adelante la "f" 
bienvenida = f"Hola {nombre} todo bien?"

# Se pueden eliminar variables
del bienvenida

